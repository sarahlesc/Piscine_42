int	ft_strcmp(char *s1, char *s2)
{
	unsigned int i;
	unsigned int n;

	i = 0;
	n = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			n = s1[i] - s2[i];
			return (n);
		}
		i++;
	}
	if ((s1[i] != '\0' && s2[i] == '\0') || (s1[i] == '\0' && s2[i] != '\0'))
	{
		n = s1[i] - s2[i];
		return (n);
	}
	return (0);
}
