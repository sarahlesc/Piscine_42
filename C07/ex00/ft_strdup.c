#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*a;
	int		i;

	a = malloc(sizeof(*src));
	if (a == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		a[i] = src[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
